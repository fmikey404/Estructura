    private void btBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBorrarActionPerformed
        try{
            int id = Integer.parseInt(JOptionPane.showInputDialog(this, "Ingrese el ID del estudiante a borrar.", "Consultar", JOptionPane.QUESTION_MESSAGE));
            if (Gestion.estudiantes.get(id) == null) {
            JOptionPane.showMessageDialog(this, "No esxiste el estudiante.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
            estudiantes.remove(id);
            JOptionPane.showMessageDialog(this, "Estudiante borrado con éxito", "Estudiante borrado", JOptionPane.INFORMATION_MESSAGE);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "El ID debe ser numérico.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btBorrarActionPerformed

