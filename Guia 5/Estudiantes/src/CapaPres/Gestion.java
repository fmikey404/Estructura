    private void btConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConsultarActionPerformed
        try{
            int id = Integer.parseInt(JOptionPane.showInputDialog(this, "Ingrese el ID del estudiante a consultar.", "Consultar", JOptionPane.QUESTION_MESSAGE));
        JOptionPane.showMessageDialog(this, "Estudiante:"
                + "\nID: "+id
                + "\nNombres: "+estudiantes.get(id).getNombre()
                + "\nApellidos: "+estudiantes.get(id).getApellido()
                + "\nDirección: "+estudiantes.get(id).getDireccion()
                + "\nTeléfono: "+estudiantes.get(id).getTelefono()
                + "\nCarrera: "+estudiantes.get(id).getCarrera());
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(this, "No esxiste el estudiante.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "El ID debe ser numérico.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btConsultarActionPerformed

