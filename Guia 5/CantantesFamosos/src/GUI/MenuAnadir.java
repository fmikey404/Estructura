    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            String nombre = this.txtFldNombre.getText();
            String disco = this.txtFldDisco.getText();
            int copias = Integer.parseInt(this.txtFldCopias.getText());
            CantanteFamoso cantante = new CantanteFamoso(nombre, disco, copias);
            ListaCantantesFamosos.listaCantantesFamosos.add(cantante);
            Principal inicio = new Principal();
            inicio.setVisible(true);
            this.dispose();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "EN LA CANTIDAD DE COPIAS DEBEN SER VALORES NUMERICOS"
                    + " ENTEROS", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

